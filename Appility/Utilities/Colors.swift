import UIKit

extension UIColor {
    static var appEmerald : UIColor {
        get {
            return UIColor(red: 230/255.0, green: 92/255.0, blue: 0, alpha: 1.0)
        }
    }
    
    static var appNightBlue : UIColor {
        get {
            return UIColor(red: 255/255.0, green: 198/255.0, blue: 67/255.0, alpha: 1.0)
        }
    }
    
    static var appClouds : UIColor {
        get {
            return UIColor(red: 152/255.0, green: 148/255.0, blue: 97/255.0, alpha: 1.0)
        }
    }
}
